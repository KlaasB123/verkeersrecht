// Helper function to get term by slug
export const getTermBySlug = (slug: string): AZTermContent | undefined => {
  return azTermsContent.find(term => term.slug === slug);
};

// Get all terms for a specific letter
export const getTermsByLetter = (letter: string): AZTermContent[] => {
  return azTermsContent.filter(term => term.letter === letter.toUpperCase());
};
